package org.openspcoop2.pdd.core.behaviour.conditional;

import java.io.Serializable;
import java.util.List;

import org.openspcoop2.generic_project.beans.IEnumeration;
import org.openspcoop2.generic_project.exception.NotFoundException;

/**     
 * TipoSelettore
 *
 * @author Poli Andrea (poli@link.it)
 * @author $Author$
 * @version $Rev$, $Date$
 */
public enum TipoSelettore implements IEnumeration , Serializable , Cloneable {

	HEADER_BASED ("HeaderBased"),
	URLBASED ("URLBased"),
	FORM_BASED ("FormBased"),
	SOAPACTION_BASED ("SOAPActionBased"),
	CONTENT_BASED ("ContentBased"),
	INDIRIZZO_IP ("ClientIP"),
	INDIRIZZO_IP_FORWARDED ("XForwardedFor"),
	TEMPLATE ("Template"),
	VELOCITY_TEMPLATE ("VelocityTemplate"),
	FREEMARKER_TEMPLATE ("FreemarkerTemplate");
	
	
	/** Value */
	private String value;
	@Override
	public String getValue()
	{
		return this.value;
	}


	/** Official Constructor */
	TipoSelettore(String value)
	{
		this.value = value;
	}

	
	public boolean isTemplate() {
		return TEMPLATE.equals(this) || VELOCITY_TEMPLATE.equals(this) || FREEMARKER_TEMPLATE.equals(this);
	}
	
	public boolean hasParameter() {
		return HEADER_BASED.equals(this) || URLBASED.equals(this) || FORM_BASED.equals(this) || CONTENT_BASED.equals(this) || this.isTemplate();
	}

	
	@Override
	public String toString(){
		return this.value;
	}
	public boolean equals(String object){
		if(object==null)
			return false;
		return object.equals(this.getValue());	
	}
	
		
	
	/** compatibility with the generated bean (reflection) */
	public boolean equals(Object object,List<String> fieldsNotCheck){
		if( !(object instanceof TipoSelettore) ){
			throw new RuntimeException("Wrong type: "+object.getClass().getName());
		}
		return this.equals(((TipoSelettore)object));
	}
	public String toString(boolean reportHTML){
		return toString();
	}
  	public String toString(boolean reportHTML,List<String> fieldsNotIncluded){
  		return toString();
  	}
  	public String diff(Object object,StringBuilder bf,boolean reportHTML){
		return bf.toString();
	}
	public String diff(Object object,StringBuilder bf,boolean reportHTML,List<String> fieldsNotIncluded){
		return bf.toString();
	}
	
	
	/** Utilities */
	
	public static String[] toArray(){
		String[] res = new String[values().length];
		int i=0;
		for (TipoSelettore tmp : values()) {
			res[i]=tmp.getValue();
			i++;
		}
		return res;
	}	
	public static String[] toStringArray(){
		String[] res = new String[values().length];
		int i=0;
		for (TipoSelettore tmp : values()) {
			res[i]=tmp.toString();
			i++;
		}
		return res;
	}
	public static String[] toEnumNameArray(){
		String[] res = new String[values().length];
		int i=0;
		for (TipoSelettore tmp : values()) {
			res[i]=tmp.name();
			i++;
		}
		return res;
	}
	
	public static boolean contains(String value){
		return toEnumConstant(value)!=null;
	}
	
	public static TipoSelettore toEnumConstant(String value){
		try{
			return toEnumConstant(value,false);
		}catch(NotFoundException notFound){
			return null;
		}
	}
	public static TipoSelettore toEnumConstant(String value, boolean throwNotFoundException) throws NotFoundException{
		TipoSelettore res = null;
		for (TipoSelettore tmp : values()) {
			if(tmp.getValue().equals(value)){
				res = tmp;
				break;
			}
		}
		if(res==null && throwNotFoundException){
			throw new NotFoundException("Enum with value ["+value+"] not found");
		}
		return res;
	}
	
	public static IEnumeration toEnumConstantFromString(String value){
		try{
			return toEnumConstantFromString(value,false);
		}catch(NotFoundException notFound){
			return null;
		}
	}
	public static IEnumeration toEnumConstantFromString(String value, boolean throwNotFoundException) throws NotFoundException{
		TipoSelettore res = null;
		for (TipoSelettore tmp : values()) {
			if(tmp.toString().equals(value)){
				res = tmp;
				break;
			}
		}
		if(res==null && throwNotFoundException){
			throw new NotFoundException("Enum with value ["+value+"] not found");
		}
		return res;
	}
}
