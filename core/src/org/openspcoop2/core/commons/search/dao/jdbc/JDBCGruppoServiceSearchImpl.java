	private Gruppo _get(JDBCServiceManagerProperties jdbcProperties, Logger log, Connection connection, ISQLQueryObject sqlQueryObject, Long tableId, org.openspcoop2.generic_project.beans.IDMappingBehaviour idMappingResolutionBehaviour) throws NotFoundException, MultipleResultException, NotImplementedException, ServiceException, Exception {
	
		org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities jdbcUtilities = 
					new org.openspcoop2.generic_project.dao.jdbc.utils.JDBCPreparedStatementUtilities(sqlQueryObject.getTipoDatabaseOpenSPCoop2(), log, connection);
		
		ISQLQueryObject sqlQueryObjectGet = sqlQueryObject.newSQLQueryObject();
				
		Gruppo gruppo = new Gruppo();
		

		// Object gruppo
		ISQLQueryObject sqlQueryObjectGet_gruppo = sqlQueryObjectGet.newSQLQueryObject();
		sqlQueryObjectGet_gruppo.setANDLogicOperator(true);
		sqlQueryObjectGet_gruppo.addFromTable(this.getGruppoFieldConverter().toTable(Gruppo.model()));
		sqlQueryObjectGet_gruppo.addSelectField("id");
		sqlQueryObjectGet_gruppo.addSelectField(this.getGruppoFieldConverter().toColumn(Gruppo.model().NOME,true));
		sqlQueryObjectGet_gruppo.addSelectField(this.getGruppoFieldConverter().toColumn(Gruppo.model().SERVICE_BINDING,true));
		sqlQueryObjectGet_gruppo.addWhereCondition("id=?");

		// Get gruppo
		gruppo = (Gruppo) jdbcUtilities.executeQuerySingleResult(sqlQueryObjectGet_gruppo.createSQLQuery(), jdbcProperties.isShowSql(), Gruppo.model(), this.getGruppoFetch(),
			new JDBCObject(tableId,Long.class));



		
        return gruppo;  
	
	} 
	
